// Utility functions for text formatting

// Convert text to uppercase for consistent display
export function toUpperCase(text: string): string {
  return text.toUpperCase();
}

// Format menu items to uppercase
export function formatMenuText(text: string): string {
  return text.toUpperCase();
}

// Format date to uppercase
export function formatDateToUpperCase(date: string): string {
  return date.toUpperCase();
}